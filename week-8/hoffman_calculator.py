#addition function
def add(number01, number02):
    return number01 + number02

#subtraction function
def subtract(number01, number02):
    return number01 - number02

#division function
def divide(number01, number02):
    return number01/number02


#outputs
print(add(1,2))
print(subtract(4,1))
print(divide(8,2))
